function slugifyName(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '.').replace(/(^\.|\.$)/g, '');
}

function createApplicants(jobId, title, statuses, offset = 0) {
  return statuses.map((status, index) => {
    const candidateName = APPLICANT_NAMES[(offset + index) % APPLICANT_NAMES.length];
    return {
      id: Number(`${jobId}${index + 1}`),
      jobId,
      candidateName,
      candidateEmail: `${slugifyName(candidateName)}@email.com`,
      title,
      status,
      score: (3.8 + (((offset + index) % 7) * 0.18)).toFixed(1),
      dateApplied: `${Math.max(1, 13 - index)} July, 2021`,
    };
  });
}

